/*
 * C99バージョン - 必要に応じてC99機能を使用
 *
 * フォーマット出力のデモ
 * printf関数の様々な使い方
 */
#include <stdio.h>

int main(void)
{
    printf("=== フォーマット出力デモ ===\n");
    
    /*
 * C99バージョン - 必要に応じてC99機能を使用
 * 基本的な文字列出力 */
    printf("こんにちは、世界！\n");
    
    /*
 * C99バージョン - 必要に応じてC99機能を使用
 * 整数の出力 */
    printf("整数: %d\n", 42);
    
    /*
 * C99バージョン - 必要に応じてC99機能を使用
 * 文字の出力 */
    printf("文字: %c\n", 'A');
    
    /*
 * C99バージョン - 必要に応じてC99機能を使用
 * 浮動小数点数の出力 */
    printf("小数: %.2f\n", 3.14159);
    
    /*
 * C99バージョン - 必要に応じてC99機能を使用
 * 文字列の出力 */
    printf("文字列: %s\n", "プログラミング");
    
    return 0;
}