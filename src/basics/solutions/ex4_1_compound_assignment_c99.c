/*
 * 課題4-1の解答例: 複合代入演算子の練習
 * ファイル名: ex4_1_compound_assignment_c99.c
 * 説明: 複合代入演算子を使った変数の段階的変更（C99版）
 * 規格: C99準拠
 */

#include <stdio.h>

int main(void)
{
    // 変数の宣言と初期化
    int x = 50;
    
    // 複合代入演算子の実演
    printf("=== 複合代入演算子の練習 ===\n");
    printf("初期値: x = %d\n", x);
    printf("\n");
    
    // += 演算子（加算代入）
    x += 15;  // x = x + 15 と同じ
    printf("x += 15:  x = %d\n", x);
    
    // -= 演算子（減算代入）
    x -= 10;  // x = x - 10 と同じ
    printf("x -= 10:  x = %d\n", x);
    
    // *= 演算子（乗算代入）
    x *= 2;   // x = x * 2 と同じ
    printf("x *= 2:   x = %d\n", x);
    
    // /= 演算子（除算代入）
    x /= 5;   // x = x / 5 と同じ
    printf("x /= 5:   x = %d\n", x);
    
    // %= 演算子（剰余代入）
    x %= 7;   // x = x % 7 と同じ
    printf("x %%= 7:   x = %d\n", x);
    
    printf("\n最終値: x = %d\n", x);
    
    return 0;
}

/*
C99での改善点:
1. // 形式のコメント使用
2. コードの可読性向上
3. その他の機能（この例では基本的な内容のため差は少ない）

計算過程は同じ:
初期値: 50 → 65 → 55 → 110 → 22 → 1
*/