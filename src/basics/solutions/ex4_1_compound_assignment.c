/*
 * 課題4-1の解答例: 複合代入演算子の練習
 * ファイル名: ex4_1_compound_assignment.c
 * 説明: 複合代入演算子を使った変数の段階的変更
 * 規格: C90準拠
 */

#include <stdio.h>

int main(void)
{
    /* 変数の宣言と初期化 */
    int x = 50;
    
    /* 複合代入演算子の実演 */
    printf("=== 複合代入演算子の練習 ===\n");
    printf("初期値: x = %d\n", x);
    printf("\n");
    
    /* += 演算子（加算代入） */
    x += 15;  /* x = x + 15 と同じ */
    printf("x += 15:  x = %d\n", x);
    
    /* -= 演算子（減算代入） */
    x -= 10;  /* x = x - 10 と同じ */
    printf("x -= 10:  x = %d\n", x);
    
    /* *= 演算子（乗算代入） */
    x *= 2;   /* x = x * 2 と同じ */
    printf("x *= 2:   x = %d\n", x);
    
    /* /= 演算子（除算代入） */
    x /= 5;   /* x = x / 5 と同じ */
    printf("x /= 5:   x = %d\n", x);
    
    /* %= 演算子（剰余代入） */
    x %= 7;   /* x = x % 7 と同じ */
    printf("x %%= 7:   x = %d\n", x);
    
    printf("\n最終値: x = %d\n", x);
    
    return 0;
}

/*
学習ポイント:
1. 複合代入演算子の種類:
   - += : 加算代入
   - -= : 減算代入  
   - *= : 乗算代入
   - /= : 除算代入
   - %= : 剰余代入

2. 動作の意味:
   - x += a は x = x + a と同じ
   - より簡潔で効率的な記述

3. 実行順序:
   - 各演算が順番に実行される
   - 前の結果が次の演算の入力となる

計算過程:
初期値: 50
50 + 15 = 65
65 - 10 = 55  
55 * 2 = 110
110 / 5 = 22
22 % 7 = 1
*/