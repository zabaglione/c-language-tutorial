/*
 * 課題4-2の解答例: インクリメント・デクリメント実験
 * ファイル名: ex4_2_increment_decrement.c
 * 説明: 前置と後置の違いを確認するプログラム
 * 規格: C90準拠
 */

#include <stdio.h>

int main(void)
{
    /* 変数の宣言と初期化 */
    int a = 10;
    int b = 20;
    int result_a, result_b;
    
    /* 初期値の表示 */
    printf("=== インクリメント・デクリメント実験 ===\n");
    printf("初期値: a = %d, b = %d\n", a, b);
    printf("\n");
    
    /* 前置インクリメント: 先に増加してから値を返す */
    printf("前置インクリメント:\n");
    result_a = ++a;  /* aを先に1増加させてから、その値をresult_aに代入 */
    printf("  ++a = %d, a = %d\n", result_a, a);
    
    /* 後置インクリメント: 値を返してから増加 */
    printf("後置インクリメント:\n");
    result_b = b++;  /* bの現在値をresult_bに代入してから、bを1増加 */
    printf("  b++ = %d, b = %d\n", result_b, b);
    
    printf("\n");
    
    /* 前置デクリメント: 先に減少してから値を返す */
    printf("前置デクリメント:\n");
    result_a = --a;  /* aを先に1減少させてから、その値をresult_aに代入 */
    printf("  --a = %d, a = %d\n", result_a, a);
    
    /* 後置デクリメント: 値を返してから減少 */
    printf("後置デクリメント:\n");
    result_b = b--;  /* bの現在値をresult_bに代入してから、bを1減少 */
    printf("  b-- = %d, b = %d\n", result_b, b);
    
    printf("\n最終値: a = %d, b = %d\n", a, b);
    
    return 0;
}

/*
学習ポイント:
1. 前置と後置の違い:
   - 前置 (++a, --a): 先に変数の値を変更してから、その値を返す
   - 後置 (a++, a--): 現在の値を返してから、変数の値を変更

2. 実行順序:
   - 前置: 増加/減少 → 値の取得
   - 後置: 値の取得 → 増加/減少

3. 実用的な使い分け:
   - forループでは通常後置を使用: for(i = 0; i < 10; i++)
   - 戻り値を使用する場合は前置/後置の違いが重要

実行結果の説明:
a: 10 → ++a(11) → --a(10)
b: 20 → b++(20→21) → b--(21→20)
*/